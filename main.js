function hideAll() {
    var x = document.getElementById("hideall");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

function hideAlso() {
    var x = document.getElementById("hidealso");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }